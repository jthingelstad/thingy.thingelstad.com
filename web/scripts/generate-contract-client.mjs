import { createHash } from 'node:crypto';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { basename, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import prettier from 'prettier';

const webRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const artifactPath = resolve(webRoot, 'contracts/librarian-api.v1.json');
const outputPath = resolve(webRoot, 'src/generated/librarian-contract.generated.ts');

function pascalCase(value) {
  return String(value)
    .replace(/(^|[^a-zA-Z0-9]+)([a-zA-Z0-9])/g, (_match, _prefix, char) => char.toUpperCase())
    .replace(/[^a-zA-Z0-9]/g, '');
}

function propertyName(value) {
  return /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(value) ? value : JSON.stringify(value);
}

function schemaType(schema = {}) {
  if (schema.$ref) return `Librarian${pascalCase(schema.$ref.split('/').at(-1))}`;
  if (schema.anyOf) return schema.anyOf.map(schemaType).join(' | ');
  if (schema.enum) return schema.enum.map((value) => JSON.stringify(value)).join(' | ');
  if (schema.type === 'array') return `Array<${schema.items ? schemaType(schema.items) : 'unknown'}>`;
  if (schema.type === 'object') {
    const entries = Object.entries(schema.properties || {});
    if (!entries.length) return 'Record<string, unknown>';
    const required = new Set(schema.required || []);
    const fields = entries.map(
      ([key, value]) => `${propertyName(key)}${required.has(key) ? '' : '?'}: ${schemaType(value)};`
    );
    return `{ ${fields.join(' ')} [key: string]: unknown; }`;
  }
  if (schema.type === 'null') return 'null';
  if (['string', 'number', 'boolean'].includes(schema.type)) return schema.type;
  return 'unknown';
}

function generatedTypes(contract) {
  return Object.entries(contract.$defs || {})
    .map(([name, schema]) => {
      const typeName = `Librarian${pascalCase(name)}`;
      if (schema.type !== 'object') return `export type ${typeName} = ${schemaType(schema)};`;
      const required = new Set(schema.required || []);
      const fields = Object.entries(schema.properties || {}).map(
        ([key, value]) => `  ${propertyName(key)}${required.has(key) ? '' : '?'}: ${schemaType(value)};`
      );
      return `export interface ${typeName} {\n${fields.join('\n')}\n  [key: string]: unknown;\n}`;
    })
    .join('\n\n');
}

function generatedSource(contract, artifactContent) {
  const checksum = createHash('sha256').update(artifactContent).digest('hex');
  return `// Generated by scripts/generate-contract-client.mjs from contracts/${basename(artifactPath)}.
// Do not edit this file directly.

export interface LibrarianContractSchema {
  $ref?: string;
  anyOf?: LibrarianContractSchema[];
  enum?: unknown[];
  type?: string;
  properties?: Record<string, LibrarianContractSchema>;
  required?: string[];
  items?: LibrarianContractSchema;
}

export interface LibrarianContractIssue {
  instancePath: string;
  message: string;
}

interface LibrarianContractArtifact {
  version: string;
  $defs: Record<string, LibrarianContractSchema>;
  endpoints: Record<string, { schema: LibrarianContractSchema; actions: Record<string, LibrarianContractSchema> }>;
  stream_events: Record<string, LibrarianContractSchema>;
}

${generatedTypes(contract)}

export const LIBRARIAN_CONTRACT_SHA256 = '${checksum}';
export const LIBRARIAN_CONTRACT = ${JSON.stringify(contract, null, 2)} as LibrarianContractArtifact;
export const LIBRARIAN_CONTRACT_VERSION = LIBRARIAN_CONTRACT.version;

function contractDefinition(ref: string) {
  const prefix = '#/$defs/';
  return ref.startsWith(prefix) ? LIBRARIAN_CONTRACT.$defs[ref.slice(prefix.length)] : undefined;
}

export function validateContractSchema(
  value: unknown,
  schema: LibrarianContractSchema,
  instancePath = ''
): LibrarianContractIssue[] {
  if (schema.$ref) {
    const definition = contractDefinition(schema.$ref);
    return definition
      ? validateContractSchema(value, definition, instancePath)
      : [{ instancePath, message: \`references unknown schema \${schema.$ref}\` }];
  }
  if (schema.anyOf) {
    if (schema.anyOf.some((candidate) => validateContractSchema(value, candidate, instancePath).length === 0)) return [];
    return [{ instancePath, message: 'must match an allowed shape' }];
  }
  if (schema.enum && !schema.enum.some((candidate) => Object.is(candidate, value))) {
    return [{ instancePath, message: 'must be an allowed value' }];
  }
  if (schema.type === 'object') {
    if (!value || typeof value !== 'object' || Array.isArray(value)) return [{ instancePath, message: 'must be object' }];
    const record = value as Record<string, unknown>;
    const issues: LibrarianContractIssue[] = [];
    for (const key of schema.required || []) {
      if (!Object.prototype.hasOwnProperty.call(record, key)) {
        issues.push({ instancePath, message: \`must have required property '\${key}'\` });
      }
    }
    for (const [key, propertySchema] of Object.entries(schema.properties || {})) {
      if (Object.prototype.hasOwnProperty.call(record, key)) {
        issues.push(...validateContractSchema(record[key], propertySchema, \`\${instancePath}/\${key}\`));
      }
    }
    return issues;
  }
  if (schema.type === 'array') {
    if (!Array.isArray(value)) return [{ instancePath, message: 'must be array' }];
    return schema.items
      ? value.flatMap((item, index) => validateContractSchema(item, schema.items as LibrarianContractSchema, \`\${instancePath}/\${index}\`))
      : [];
  }
  if (schema.type === 'null') return value === null ? [] : [{ instancePath, message: 'must be null' }];
  if (schema.type && typeof value !== schema.type) return [{ instancePath, message: \`must be \${schema.type}\` }];
  return [];
}

export function validateEndpointContract(value: unknown, context: string, action = '') {
  const endpoint = Object.keys(LIBRARIAN_CONTRACT.endpoints).find((path) => context.includes(path));
  const endpointContract = endpoint ? LIBRARIAN_CONTRACT.endpoints[endpoint] : null;
  const schema = endpointContract?.actions[action] || endpointContract?.schema || { $ref: '#/$defs/apiResponse' };
  return validateContractSchema(value, schema);
}

export function validateStreamContract(eventName: string, value: unknown) {
  return validateContractSchema(value, LIBRARIAN_CONTRACT.stream_events[eventName] || { $ref: '#/$defs/streamBase' });
}
`;
}

async function generateContractClient({ check = false } = {}) {
  const artifactContent = await readFile(artifactPath, 'utf8');
  const contract = JSON.parse(artifactContent);
  if (!/^\d+\.\d+\.\d+$/.test(String(contract.version || ''))) {
    throw new Error('The Librarian contract must declare a semantic version.');
  }
  const formatted = await prettier.format(generatedSource(contract, artifactContent), {
    parser: 'typescript',
    printWidth: 120,
    singleQuote: true,
    trailingComma: 'none'
  });
  if (check) {
    const current = await readFile(outputPath, 'utf8').catch(() => '');
    if (current !== formatted)
      throw new Error('Generated Librarian contract client is stale. Run npm run contract:generate.');
    return;
  }
  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, formatted);
  process.stdout.write(`${outputPath}\n`);
}

if (import.meta.main) {
  generateContractClient({ check: process.argv.includes('--check') }).catch((error) => {
    console.error(error.message);
    process.exitCode = 1;
  });
}

export { generateContractClient };
